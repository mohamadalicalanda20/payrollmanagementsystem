    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed

        Main main = new Main();

        String username = tfUsername.getText();
        String password = String.valueOf(pfPassword.getPassword());

        String query = "SELECT * FROM `accounts` WHERE `username`=? AND `password`=?";

        try
        {
            pst = con.prepareStatement(query);

            pst.setString(1, username);
            pst.setString(2, password);

            rs = pst.executeQuery();

            boolean checkUser = main.checkUsername(username);
            boolean checkPass = main.checkPassword(password);

            if(rs.next())
            {
                // Retrieve id of admin after login
                int id = rs.getInt(1);
                Main.adminId = id;
                
                JOptionPane.showMessageDialog(null, "Login Successfully");
                Employee emp = new Employee();
                this.hide();
                emp.show();
                
                
                try{
                    // Retrieve admin_id and admin_name that will match to the inputted username in login form
                    String query2 = "SELECT `id`, `name` FROM `accounts` WHERE username=?";
                    pst = con.prepareStatement(query2);
                    pst.setString(1, tfUsername.getText());
                    rs = pst.executeQuery();
                    
                    if(rs.next()){
                        // Get current time and date
                        Calendar cal = new GregorianCalendar();
                        int month = cal.get(Calendar.MONTH);
                        int year = cal.get(Calendar.YEAR);
                        int day = cal.get(Calendar.DAY_OF_MONTH);
                        
                        int second = cal.get(Calendar.SECOND);
                        int minute = cal.get(Calendar.MINUTE);
                        int hour = cal.get(Calendar.HOUR);

                        // retrieved data
                        String adminId = rs.getString("id");
                        String adminName = rs.getString("name");
                        String date = ((month+1)+"/"+day+"/"+year);
                        String time = ((hour+00)+":"+(minute+00)+":"+(second+00));
                        
                        // for description
                        String desc = "Logged in the system";
                        
                        // Insert retrieved data into account login
                        String query3 = "INSERT INTO `account_login`(`admin_id`, `admin_name`, `login_date`, `login_time`, `description`) VALUES (?, ?, ?, ?, ?)";
                        pst = con.prepareStatement(query3);
                        pst.setString(1, adminId);
                        pst.setString(2, adminName);
                        pst.setString(3, date);
                        pst.setString(4, time);
                        pst.setString(5, desc);
                        
                        pst.execute();
                    }
                    
                }catch(Exception e){
                    
                }
            }
            else{
                if((tfUsername.getText()).equals("")){
                    JOptionPane.showMessageDialog(null, "Please enter your username and password.");
                }
                else if(checkUser == false){
                    JOptionPane.showMessageDialog(null, "Username does not exist.");
                }
                else if((pfPassword.getText()).equals("")){
                    JOptionPane.showMessageDialog(null, "Please enter your password.");
                }
                else if(checkPass == false){
                    JOptionPane.showMessageDialog(null, "Password is incorrect.");
                }
            }
            
        }
        catch(SQLException ex)
        {
            
        }

    }//GEN-LAST:event_btnLoginActionPerformed

